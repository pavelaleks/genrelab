def get_genre_by_id(genre_id: str) -> Optional[Genre]:
    """
    Находит жанр по его идентификатору.
    
    Args:
        genre_id: Идентификатор жанра
        
    Returns:
        Optional[Genre]: Объект Genre или None, если не найден
    """
    for genre in get_all_genres():
        if genre.id == genre_id:
            return genre
    return None

